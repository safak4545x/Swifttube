                                        Image(systemName: "checkmark.circle.fill")
                                            .font(.system(size: 30))
                                            .foregroundColor(.green)
                                        Text(csvFileName)
                                            .font(.headline)
                                            .foregroundColor(.primary)
                                        Text("\(csvChannelCount) \(i18n.t(.subscriptions))")
                                            .font(.subheadline)
                                            .foregroundColor(.secondary)
                                    }
                                } else {
                                    VStack(spacing: 8) {
