    func handleCSVDrop(providers: [NSItemProvider]) -> Bool {
        print("üîç handleCSVDrop √ßaƒürƒ±ldƒ±, provider sayƒ±sƒ±: \(providers.count)")
        guard let provider = providers.first else { 
            print("‚ùå Provider bulunamadƒ±")
            return false 
        }
        
        print("üîç Provider type identifiers: \(provider.registeredTypeIdentifiers)")
        
        // EN: Try UTType.fileURL first. TR: √ñnce UTType.fileURL ile dene.
        if provider.hasItemConformingToTypeIdentifier(UTType.fileURL.identifier) {
            provider.loadItem(forTypeIdentifier: UTType.fileURL.identifier, options: nil) { (urlData, error) in
                DispatchQueue.main.async {
                    if let error = error {
                        print("‚ùå loadItem hatasƒ±: \(error.localizedDescription)")
                        self.csvProcessMessage = "\(i18n.t(.fileLoadErrorPrefix)): \(error.localizedDescription)"
                        return
                    }
                    
                    if let url = urlData as? URL {
                        print("‚úÖ CSV dosyasƒ± URL'si alƒ±ndƒ±: \(url.lastPathComponent)")
                        self.csvFileName = url.lastPathComponent
                        self.analyzeCSVFile(url: url)
                    } else if let urlData = urlData as? Data, let urlString = String(data: urlData, encoding: .utf8), let url = URL(string: urlString) {
                        print("‚úÖ CSV dosyasƒ± URL'si data'dan alƒ±ndƒ±: \(url.lastPathComponent)")
                        self.csvFileName = url.lastPathComponent
                        self.analyzeCSVFile(url: url)
                    } else {
                        print("‚ùå URL d√∂n√º≈üt√ºrme hatasƒ±")
            self.csvProcessMessage = i18n.t(.fileFormatNotSupported)
                    }
                }
            }
        } else {
            print("‚ùå FileURL type identifier desteklenmiyor")
            DispatchQueue.main.async {
        self.csvProcessMessage = i18n.t(.unsupportedFileType)
            }
            return false
        }
        
        return true
    }
    
    func analyzeCSVFile(url: URL) {
    print("üîç CSV dosyasƒ± analiz ba≈ülƒ±yor: \(url.lastPathComponent)")
    csvProcessMessage = i18n.t(.processingCSV)
        
        do {
            let content = try String(contentsOf: url, encoding: .utf8)
            print("‚úÖ CSV dosyasƒ± okundu, karakter sayƒ±sƒ±: \(content.count)")
            
            let lines = content.components(separatedBy: .newlines)
            print("üìÑ CSV satƒ±r sayƒ±sƒ±: \(lines.count)")
            
            guard lines.count > 1 else { 
                print("‚ùå Ge√ßersiz CSV formatƒ± - yeterli satƒ±r yok")
                csvProcessMessage = i18n.t(.invalidCSV)
                return 
            }
            
            // ƒ∞lk satƒ±rdan kolon ba≈ülƒ±klarƒ±nƒ± al
            let headers = lines[0].components(separatedBy: ",")
            print("üìù CSV ba≈ülƒ±klarƒ±: \(headers)")
            var urlColumnIndex = -1
            
            // URL kolonunu bul (T√ºrk√ße ve ƒ∞ngilizce ba≈ülƒ±klarƒ± destekle)
            for (index, header) in headers.enumerated() {
                let cleanHeader = header.trimmingCharacters(in: .whitespacesAndNewlines).lowercased()
                if cleanHeader.contains("channel url") || 
                   cleanHeader.contains("kanal url") ||
                   cleanHeader.contains("url") {
                    urlColumnIndex = index
                    print("‚úÖ URL kolonu bulundu: index \(index), ba≈ülƒ±k: '\(header)'")
                    break
                }
            }
            
            // URL kolonu bulunamazsa ikinci kolonu dene
            if urlColumnIndex == -1 && headers.count >= 2 {
                urlColumnIndex = 1
                print("‚ö†Ô∏è URL kolonu bulunamadƒ±, ikinci kolonu deneyeceƒüiz: index 1")
            }
            
            guard urlColumnIndex != -1 else { 
                print("‚ùå CSV'de URL kolonu bulunamadƒ±")
                csvProcessMessage = i18n.t(.csvURLColumnNotFound)
                return 
            }
            
            // URL'leri √ßƒ±kar
            var channelURLs: [String] = []
            
            for (lineIndex, line) in lines.dropFirst().enumerated() {
                if line.isEmpty { continue }
                
                let columns = line.components(separatedBy: ",")
                if columns.count > urlColumnIndex {
                    let url = columns[urlColumnIndex].trimmingCharacters(in: .whitespacesAndNewlines).replacingOccurrences(of: "\"", with: "")
                    if !url.isEmpty && url.contains("youtube.com") {
                        channelURLs.append(url)
                        if lineIndex < 3 { // ƒ∞lk 3 URL'yi log'la
                            print("‚úÖ Ge√ßerli URL bulundu: \(url)")
                        }
                    } else if !url.isEmpty {
                        if lineIndex < 3 { // ƒ∞lk 3 ge√ßersiz URL'yi log'la
                            print("‚ö†Ô∏è Ge√ßersiz URL atlandƒ±: \(url)")
                        }
                    }
                }
            }
            
            print("‚úÖ CSV'den \(channelURLs.count) kanal URL'i bulundu")
            
            // Sonu√ßlarƒ± kaydet
            csvChannelURLs = channelURLs
            csvChannelCount = channelURLs.count
            csvFileDropped = true
            csvProcessMessage = ""
            print("‚úÖ CSV analizi tamamlandƒ±: csvFileDropped = \(csvFileDropped)")
            
        } catch {
            print("‚ùå CSV dosyasƒ± okunurken hata: \(error)")
            csvProcessMessage = "\(i18n.t(.fileLoadErrorPrefix)): \(error.localizedDescription)"
        }
    }
    
    func processCSVFile() {
        guard !csvChannelURLs.isEmpty else { 
            print("‚ùå processCSVFile: csvChannelURLs bo≈ü")
            return 
        }
        
        print("üîÑ processCSVFile ba≈ülƒ±yor: \(csvChannelURLs.count) URL i≈ülenecek")
        print("üîó ƒ∞lk 3 URL: \(Array(csvChannelURLs.prefix(3)))")
        
        isProcessingCSV = true
        processProgress = 0.0
        csvProcessMessage = "Abonelikler y√ºkleniyor..."
        
        // Progress bar animasyonu
        withAnimation(.linear(duration: 3.0)) {
            processProgress = 1.0
        }
        
        // Kanal URL'lerini i≈üle
        print("üöÄ youtubeAPI.processBatchChannelURLs √ßaƒürƒ±lƒ±yor")
        youtubeAPI.processBatchChannelURLs(csvChannelURLs)
        
        // 3 saniye sonra modal'ƒ± kapat
        DispatchQueue.main.asyncAfter(deadline: .now() + 3.0) {
            print("‚úÖ processCSVFile tamamlandƒ±, modal kapanƒ±yor")
            onCancel()
        }
    }
}
